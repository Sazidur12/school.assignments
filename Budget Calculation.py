# জাহিদ জিনিসপত্র ক্রয়ের জন্য ১৫০০০ টাকা নিয়ে সুপার শপে গেলেন। সুপার শপে তার জিনিসপত্রে মোট বিল আসল ১৩২৫০.৫০ টাকা। সুপার শপ তাকে একটা ভাউচার রশিদ দিলো, সেখানে vegetable price = 200, fish price 2000, meat price = 3000, milk price=300, vat rate= 15.50 টাকা উল্লেখ ছিলো। জাহিদের মোট খরচ বিল কত হলো এবং তার কাছে আর কত টাকা আছে পাইথন প্রোগ্রামের মাধ্যমে লেখো।

# Initial total balance
total_balance = 15000

# Prices of items
vegetable_price = 200
fish_price = 2000
meat_price = 3000
milk_price = 300
vat_rate = 15.50

# Calculate the total cost
total_cost = vegetable_price + fish_price + meat_price + milk_price + vat_rate

# Calculate the present balance
present_balance = total_balance - total_cost

# Print the total cost and present balance
print("Total Cost of Jahid is: ", total_cost)
print("Present Balance of Jahid is: ", present_balance)
